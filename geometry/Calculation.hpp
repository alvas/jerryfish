#pragma once

#ifndef CALCULATION
#define CALCULATION

#include "Point.hpp"

double cross(const Point &O, const Point &A, const Point &B)
{
    return (A.x - O.x) * (B.y - O.y) - (A.y - O.y) * (B.x - O.x);
}

Point3D cross(const Point3D &A, const Point3D &B)
{
    return {A.y * B.z - A.z * B.y, A.z * B.x - A.x * B.z, A.x * B.y - A.y * B.x};
}

#endif
